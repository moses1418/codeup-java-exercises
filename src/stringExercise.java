public class stringExercise {
    public static void main(String[] args) {

        String myString = "We don't need no education\n" +
                "We don't need no thought control";

        System.out.println(myString);

        String myOtherString = "Check \"this\" out!, \"s inside of \"s!";

        System.out.println(myOtherString);

        String thirdString = "In windows, the main drive is usually C:\\";

        System.out.println(thirdString);

        String lastString = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";

        System.out.println(lastString);
    }


}
